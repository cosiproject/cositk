package org.cosiproject.toolkit.conf;

// TODO: Auto-generated Javadoc
/**
 * The Class CfgKey.
 */
public class CfgKey {
	
	/** The text. */
	private String text;
	
	/**
	 * Instantiates a new cfg key.
	 *
	 * @param string the string
	 */
	public CfgKey(String string) {
		text = string;
	}
	
	/**
	 * Instantiates a new cfg key.
	 */
	public CfgKey() {
		
	}
	
	/**
	 * Gets the text.
	 *
	 * @return the text
	 */
	public String getText() {
		return text;
	}
	
	/**
	 * Sets the text.
	 *
	 * @param text the new text
	 */
	public void setText(String text) {
		this.text = text;
	}
	
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	public String toString() {
		return text;
	}

}
